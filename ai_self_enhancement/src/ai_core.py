import time
from capability_registry import CapabilityRegistry
from self_reflection import SelfReflection
from error_handling import log_info, log_error, AISelfEnhancementError

class AICore:
    def __init__(self):
        self.capability_registry = CapabilityRegistry()
        self.self_reflection = SelfReflection(self.capability_registry)
        self.running = False
        log_info("AICore initialized")

    def start(self):
        """Start the main AI loop."""
        self.running = True
        log_info("AICore started")
        while self.running:
            try:
                self.execute_task_cycle()
                self.perform_self_reflection()
                time.sleep(60)  # Wait for 60 seconds before the next cycle
            except AISelfEnhancementError as e:
                log_error(f"Error in AI core loop: {str(e)}")
                self.running = False

    def stop(self):
        """Stop the main AI loop."""
        self.running = False
        log_info("AICore stopped")

    def execute_task_cycle(self):
        """Execute a cycle of tasks based on current capabilities."""
        log_info("Executing task cycle")
        for capability in self.capability_registry.list_capabilities():
            task_result = self.execute_capability(capability)
            self.self_reflection.log_performance(capability, task_result["result"], task_result["execution_time"], category=task_result["category"])

